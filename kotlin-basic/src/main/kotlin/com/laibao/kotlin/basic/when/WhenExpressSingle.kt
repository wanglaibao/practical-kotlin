package com.laibao.kotlin.basic.`when`

/**
 * @author laibao wang
 */

fun main(args: Array<String>) {
    val score = 'B'
    when (score) {
        'A' -> println("safdasf")
        'B' -> println("fasfasdfa")
        'C' -> println("fafsadf")
        'D' -> println("fafaf")
        'E' -> println("fasfaf")
        else -> println("dfgdsgsad")
    }
}
